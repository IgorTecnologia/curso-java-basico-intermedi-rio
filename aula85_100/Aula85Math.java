package com.igor.cursojava.aula85_100;

public class Aula85Math {

	public static void main(String[] args) {
			
		System.out.println(Math.pow(2, 3)); // elevação a potencia
		
		System.out.println(Math.round(4.7)); // acima de 4.5 arredonda para cima
		
		System.out.println(Math.round(4.4)); // abaixo de 4.5 arredonda para baixo
		
		System.out.println(Math.ceil(4.4)); // arredonda para cima
		
		System.out.println(Math.ceil(4.7));
		
		System.out.println(Math.floor(4.7)); // arredonda para baixo
		
		System.out.println(Math.round(Math.random() * 10));
		
		System.out.println(Math.sin(Math.toRadians(30))); // sin = seno
		
		System.out.println(Math.cos(Math.toRadians(1))); // cosseno
		
		System.out.println(Math.tan(Math.toRadians(0))); // tangente
		
		
	}

}